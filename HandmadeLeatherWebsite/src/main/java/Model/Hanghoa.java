package Model;
public class Hanghoa {
private String mahh;
private String tenhh;
private int dongia;
private String xuatxu;
private String loaihh;
private String hinh;
private int xuhuong;
private int phobien;
public Hanghoa(String mahh, String tenhh, int 
dongia, String xuatxu, String loaihh, String hinh, int 
xuhuong,
int phobien) {
this.mahh = mahh;
this.tenhh = tenhh;
this.dongia = dongia;
this.xuatxu = xuatxu;
this.loaihh = loaihh;
this.hinh = hinh;
this.xuhuong = xuhuong;
this.phobien = phobien;
}
public Hanghoa() {
}
public String getMahh() {
return mahh;
}
public void setMahh(String mahh) {
this.mahh = mahh;
}
public String getTenhh() {
return tenhh;
}
public void setTenhh(String tenhh) {
this.tenhh = tenhh;
}
public int getDongia() {
return dongia;
}
public void setDongia(int dongia) {
this.dongia = dongia;
}
public String getXuatxu() {
return xuatxu;
}
public void setXuatxu(String xuatxu) {
this.xuatxu = xuatxu;
}
public String getLoaihh() {
return loaihh;
}
public void setLoaihh(String loaihh) {
this.loaihh = loaihh;
}
public String getHinh() {
return hinh;
}
public void setHinh(String hinh) {
this.hinh = hinh;
}
public int getXuhuong() {
return xuhuong;
}
public void setXuhuong(int xuhuong) {
this.xuhuong = xuhuong;
}
public int getPhobien() {
return phobien;
}
public void setPhobien(int phobien) {
this.phobien = phobien;
}
}
